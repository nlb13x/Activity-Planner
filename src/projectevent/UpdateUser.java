    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            String user=jTextField1.getText().trim();int i=0;
            //   int userint=Integer.parseInt(user);
            jTextField2.setText(user);
            Connection con=DatabaseConnection.getConn();
            String query="select * from login where id_login=?";
            PreparedStatement statement=con.prepareStatement(query);
            statement.setString(1, user);
            ResultSet rs=statement.executeQuery();
            if(rs.next()){
                jTextField2.setText(rs.getString(2));
                jComboBox1.setSelectedItem(rs.getString(3));
                jTextField3.setText(rs.getString(4));
                jPasswordField1.setText(rs.getString(5));
                jTextField4.setText(rs.getString(6));
                jTextField5.setText(rs.getString(7));
                jTextField6.setText(rs.getString(8));
                jTextField7.setText(rs.getString(9));
                 JOptionPane.showMessageDialog(null, "Change the details below and Click on Save Changes");
            }else
                JOptionPane.showMessageDialog(null, "User Not Exist");
                   
        } catch (SQLException ex) {
            Logger.getLogger(UpdateUser.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

