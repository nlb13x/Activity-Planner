    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       String loginid=null,username=null,permission=null,status=null,password=null,email=null,college=null,academicyear=null,department=null;
        try {
           
            loginid=jTextField1.getText().trim();
            username=jTextField2.getText();
            permission=jComboBox1.getSelectedItem().toString();
            status=jTextField3.getText();
            password=new String(jPasswordField1.getPassword());
            email=jTextField4.getText();
            college=jTextField5.getText();
            academicyear=jTextField6.getText();
            department=jTextField7.getText();
            Connection con=DatabaseConnection.getConn();
            String query="UPDATE `eventmanagement`.`login` SET `username`='"+username+"', `permission`='"+permission+"', `status`='"+status+"', `password`='"+password+"', `email`='"+email+"', `college`='"+college+"', `academic year`='"+academicyear+"', `department`='"+department+"' WHERE `id_login`='"+loginid+"';";
            Statement stmt=con.createStatement();
            stmt.execute(query);
            JOptionPane.showMessageDialog(null, "updated successfully");
        } catch (Exception ex) {
            Logger.getLogger(UpdateUser.class.getName()).log(Level.SEVERE, null, ex);
            if(loginid==null){
                 JOptionPane.showMessageDialog(null, "Enter LoginId and Find Then Update");
            }else
             JOptionPane.showMessageDialog(null, "Try After Some Time");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

